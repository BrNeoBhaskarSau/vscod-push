public class newGit {
    public static void main(String args[]){
        int multiply = 2;
        for(int i=1; i<=10; i++){
            System.out.println(multiply+" X "+i+" = "+i*multiply);
        }
        for(int i=1; i<=10; i++){
            System.out.println(multiply+" / "+i+" = "+i/multiply);
        }
        for(int i=1; i<=10; i++){
            System.out.println(multiply+" + "+i+" = "+i+multiply);
        }
        for(int i=1; i<=10; i++){
            System.out.println(multiply+" % "+i+" = "+i%multiply);
        }
    }
}
